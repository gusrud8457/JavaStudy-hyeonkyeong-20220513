package j06_반복;

public class For01 {

	public static void main(String[] args) {
		
		//for문 형식 : for (초기식 ; 조건식; 조건변화식) {  }
		//for 문은 규칙적인 조건으로 반복일 때!
		//while 문은 자유로운 조건으로 반복일 때!(내가 하고 싶을 때)
	
		for(int i = 0 ; i < 10; i++) {
			System.out.println(i + 1);			
		}
		for(int i = 0; i < 10; i++) {//10번 반복해라
			System.out.println(10 - i);	
			
		}
	 

	}

}
